        <ul>
          <li>
            <font face="Saab" color="#010101"><i>Note: in a sense this question probably asks why self-determined participatory actions are the preferred (one or only?) way for marginalized people to organize the struggle for new political space and demands. This refers back, on the one hand, to the reasons of actions, because those reaons won't be overcome completely in the short term, and on the other hand is refers to places and spaces where those actions can be organized, where the people can gather, hence also asks the question how those spaces and places can be maintained but also created.</i></font>
