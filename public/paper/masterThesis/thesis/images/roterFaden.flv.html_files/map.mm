        <ul>
          <li>
            <font face="Saab" color="#010101"><i>Note: This seems to be a simple question in the first place but when looking at reasons for self-determined participatory actions one has to consider the precarious situation the people life and the reasons behind that situation. Then certainly a whole universe is opening up, thus reasons in this context are probably not just related to reasons for actions and their goals and aims but also related to the reasons that produce the situation of the people, which asks for the reasons how the city the people life in affect their life and vice versa. A brief overview of different reaons can already be seen in the diagram above.</i>&#160; </font>
          </li>
        </ul>
      </li>
      <li>
        <font face="Saab" color="#010101"><b>What are the goals of conducting self-determined participatory action?</b>&#160; </font>

        <ul>
          <li>
            <font face="Saab" color="#010101"><i>Note: Goals are as heterogeneous as reaons. Goals are of political nature but tackle also the daily reality, thus are small and persistent, such as the daily survival or the formation of knowledge and education. Political goals may affect the daily realities in the mid or long run and need long lasting and persistent actions. One interesting point here would be to sketch the level of self-determination and participation in the process of conducting actions and when their goals have been achieved fully or partly.</i>&#160;fe </font>
          </li>
        </ul>
      </li>
